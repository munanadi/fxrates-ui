export const totalSum = (
  data: { name: string; total: number }[]
): string => {
  const totalSum = data.reduce(
    (acc, currVal) => (acc += currVal.total),
    0
  );
  const formatter = new Intl.NumberFormat("en-us", {
    style: "currency",
    currency: "USD",
  });

  return formatter.format(totalSum);
};
